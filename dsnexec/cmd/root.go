import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "dsnexec",
	Short: "dsnexec allow executing command when watched dsn files are changed",
	Long: `dsnexec will watch a set of files that contain dsn connection strings. 
When any of the files are changed, dsnexec will execute a command. This can be used
to modify a running application's dsn connection strings without restarting the application or
to modify postgres fdw server connection strings without restarting the server.`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

